/*
Напишите программу, которая запрашивает у пользователя температуру 
в градусах Цельсия, преобразует ее в градусы Фаренгейта и выводит 
на экран результат в виде строки. Формула для преобразования: 
F = (9/5) * C + 32, 
где F - температура в градусах Фаренгейта, 
C - температура в градусах Цельсия.

Используйте функцию prompt и alert для решения задания. 
При этом помните, что нужно преобразовать строку в число.
*/
let temperature = +prompt("Введите температуру в градусах Цельсия");
alert(`${temperature}C = ${temperature * (9/5) + 32}F`);